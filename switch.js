let day = 3;

switch (day) {
    case 1:
        console.log("Monday");
        break;

    case 2:
        console.log("Thursday");
        break;

    case 3:
        console.log("Wed");
        break;

    case 4:
        console.log("Th");
        break;

    case 5:
        console.log("Fr");
        break;

    case 6:
        console.log("Sat");
        break;

    case 7:
        console.log("Sun");
        break;

    default:
        console.log("Invalid day");

}